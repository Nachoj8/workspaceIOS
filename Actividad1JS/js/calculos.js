function suma(a,b){
    var resultado = a + b;
    return resultado;
  }

function resta(a,b){
    var resultado = a - b;
    return resultado;
}
  
  
function multiplicar(a,b){
    var resultado = a * b;
    return resultado;
}
   

function dividir(a,b){
    var resultado = a / b;
    return resultado;
}  